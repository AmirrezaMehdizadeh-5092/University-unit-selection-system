function show(arg) {
    if (document.getElementById(arg).style.visibility = "hidden") {
        document.getElementById(arg).style.visibility = "visible";
    }
}
function hide(arg) {
    document.getElementById(arg).style.visibility = "hidden"
}

function open_kesho_teacher() {
    if (document.getElementById("defmasterkesho").style.visibility = 'hidden') {
        document.getElementById("defmasterkesho").style.visibility = 'visible';
    }
    else {
        document.getElementById("defmasterkesho").style.visibility = 'hidden';
    }
}
function close_kesho_teacher() {
    if (document.getElementById("defmasterkesho").style.visibility = 'visible') {
        document.getElementById("defmasterkesho").style.visibility = 'hidden';
    }
}

